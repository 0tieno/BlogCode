public class Basics {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        int sum = a + b;
        int difference = a - b;
        int product = a * b;
        double division = (double) a / b;

        System.out.println("Sum: " + sum);
        System.out.println("Difference: " + difference);
        System.out.println("Product: " + product);
        System.out.println("Division: " + division);
    }
}
